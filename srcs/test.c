int main()
{
	double posX = 22, posY = 12;  //x and y start position
	double dirX = -1, dirY = 0; //initial direction vector
	double planeX = 0, planeY = 0.66; //the 2d raycaster version of camera plane

	double time = 0; //time of current frame
	double oldTime = 0; //time of previous frame
	t_data mlx;
	mlx.mlx_ptr = mlx_init();
	mlx.win_ptr = mlx_new_window(mlx.mlx_ptr, screenWidth, screenHeight, "Cub3D");


	for(int x = 0; x < screenWidth ; x++)
	{
		//calculate ray position and direction
		double cameraX = 2 * x / screenWidth - 1; // x-coordinate in camera space
		double rayDirX = dirX + planeX * cameraX;
		double rayDirY = dirY + planeY * cameraX;
		//dans quelle case de la carte nous nous trouvons (convertir double en int)
		int mapX = posX;
		int mapY = posY;

		//length of ray from current position to next x or y-side
	  	double sideDistX;
	  	double sideDistY;

		//longueur du rayon depuis la position actuelle jusqu'au prochain côté x ou y
		double deltaDistX;
		if (rayDirX == 0) 
			deltaDistX = 1e30;
		else 
			deltaDistX = fabs(1 / rayDirX); // abs(1 / rayDirX)
		double deltaDistY;
		if (rayDirY == 0) 
			deltaDistY = 1e30;
		else 
			deltaDistY = fabs(1 / rayDirY);// abs(1 / rayDirY)
		double perpWallDist;

		//dans quelle direction avancer dans la direction x ou y (soit +1 ou -1)
		int stepX;
	 	int stepY;

		int hit = 0; //Y a-t-il eu un coup de mur ?
	  	int side; //un mur NS ou EW a-t-il été touché ?
	
		//calculate step and initial sideDist
		if (rayDirX < 0)
		{
			stepX = -1;
			sideDistX = (posX - mapX) * deltaDistX;
		}
		else
		{
			stepX = 1;
			sideDistX = (mapX + 1.0 - posX) * deltaDistX;
		}
		if (rayDirY < 0)
		{
			stepY = -1;
			sideDistY = (posY - mapY) * deltaDistY;
		}
		else
		{
			stepY = 1;
			sideDistY = (mapY + 1.0 - posY) * deltaDistY;
		}
		//perform DDA
	  	while (hit == 0)
	  	{
			//jump to next map square, either in x-direction, or in y-direction
			if (sideDistX < sideDistY)
			{
				sideDistX += deltaDistX;
				mapX += stepX;
				side = 0;
			}
			else
			{
				sideDistY += deltaDistY;
				mapY += stepY;
				side = 1;
			}
			if (worldMap[mapX][mapY] > 0)
				hit = 1;
	  	}
		if(side == 0)
	  		perpWallDist = (sideDistX - deltaDistX);
      	else
			perpWallDist = (sideDistY - deltaDistY);
	}







  mlx_loop(mlx.mlx_ptr);
}
